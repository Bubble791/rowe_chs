def ExtractPointer(byteList: [bytes]):
    pointer = 0
    for a in range(len(byteList)):
        pointer += (int(byteList[a])) << (8 * a)

    return pointer

def ProcessString(file, offset, limit):
    file.seek(offset)
    #charmap = get_text_dict()
    text = ""
    while True:
        data = file.read(1)[0]
        if data == 0xFF:
            break
        if data == 0x1B:
            text += "é"
        elif (data > 0 and data <= 0x20) or data == 0xF9 or data == 0xFD or data == 0xF8:
            data2 = file.read(1)[0]
            data = data << 8 | data2
        if data == 0xFC:
            name = charmap[0xFC][file.read(1)[0]]
            text += name[:-1]
            text += " " + str(ExtractPointer(file.read(charMapFC[name]))) + "}"
        elif data != 0x1B:
            text += charmap[data]
        
        if file.tell() > offset + limit:
            break
    return text

