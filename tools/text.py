def format(byteslist, offset, isbattle = False):
    string = ""
    slot = 0

    if 1:
        while slot < len(byteslist):
            if byteslist[slot] == 0xF8:
                slot += 1
                param = byteslist[slot]
                if param not in charmap[0xF8]:
                    return string
                string += charmap[0xF8][param]
            elif byteslist[slot] >= 0x1 and byteslist[slot] <= 0x1E and byteslist[slot] != 0x1B:
                charindex = byteslist[slot] << 8 | byteslist[slot + 1]
                slot += 1
                string += charmap[charindex]
            elif byteslist[slot] == 0xFD:
                slot += 1
                if isbattle == True:
                    string += "{" + battleStringFD[byteslist[slot]] + "}"
                else:
                    string += charmap[0xFD][byteslist[slot]]
            elif byteslist[slot] == 0xF9:
                slot += 1
                param = byteslist[slot]
                if param in charmap[0xF9]:
                    string += charmap[0xF9][param]
                else:
                    string += f"unknow{param}"
            elif byteslist[slot] == 0xF7:
                slot += 1
                string += "{DYNAMIC " + str(byteslist[slot]) + "}"
            elif byteslist[slot] == 0xFC:
                slot += 1
                param = byteslist[slot]
                addParam = ""
                if param == 1:
                    param2 = byteslist[slot + 1]
                    addParam = COLOR_TABLE[param2] + "}"
                    slot += 1
                elif param == 19:
                    param2 = byteslist[slot + 1]
                    addParam = str(param2) + "}"
                    slot += 1
                if param not in charmap[0xFC]:
                    return string
                string += charmap[0xFC][param] + addParam
            else:
                string += charmap[byteslist[slot]]
            slot += 1
    #return f"    .area {len(byteslist)}\n    .strn \"" + string + "\"\n    .endarea\n"
    return string
